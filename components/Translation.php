    public function getTrans($word){
		$currentLang = Yii::$app->user->identity->lang;
		$transList = $this->getTranslations();
		$translatedWord = $word;
		if(!empty($transList)){
            if(!empty($transList[$currentLang])){
                $getWordTrans = $transList[$currentLang];
                if(!empty($getWordTrans)){
                    $translatedWord = isset($getWordTrans[$word]) ? $getWordTrans[$word] : $word;
                }
            }
		}

		return $translatedWord;
	}
